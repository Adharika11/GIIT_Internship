package collection.codes;


public class Unboxing {
    public static void main(String[] args) {
        Integer i =new Integer(10);
        int a;
        a=i.intValue();
        System.out.println("After Unboxing: "+a);
        Double d=44.456;
        double dd=d.doubleValue();
        System.out.println("After Unboxing: "+dd);




    }

}
